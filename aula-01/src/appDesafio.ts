function  multi(x: number, y: number, z:number): number 
{
    return x * y * z;
}

function  so(x: number, y: number, z:number): number 
{
    return x + y + z;
}

function  div(x: number, y: number, z:number): number 
{
    return x / y / z;
}
function  sub(x: number, y: number, z:number): number 
{
    return x - y - z;
}

